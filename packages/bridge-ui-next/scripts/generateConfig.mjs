// ---------------------------------------------------------------------------
// customTokenConfig — generateTokens (TokenType.<X> rewrite + value import)
// ---------------------------------------------------------------------------
const _formatTokensToTsLiteral = (tokens) => {
  const formatToken = (token) => {
    const entries = Object.entries(token);
    const formattedEntries = entries.map(([key, value]) => {
      if (key === "type" && typeof value === "string") {
        return `${key}: TokenType.${value}`;
      }
      if (typeof value === "object") {
        return `${key}: ${JSON.stringify(value)}`;
      }
      return `${key}: ${JSON.stringify(value)}`;
    });
    return `{${formattedEntries.join(", ")}}`;
  };

  return `[${tokens.map(formatToken).join(", ")}]`;
};

async function generateCustomTokenConfig() {
  const pluginName = "generateTokens";
  const logger = new PluginLogger(pluginName);
  logger.info("Plugin initialized.");

  if (skip) {
    const body =
      `import type { Token } from '$libs/token';\n\n` +
      `export const customToken: Token[] = [];\n`;
    const out = await writeGenerated("customTokenConfig.ts", pluginName, body);
    logger.info(`Skipped token.`);
    logger.info(`Formatted config file saved to ${out}`);
    return;
  }

  if (!process.env.CONFIGURED_CUSTOM_TOKENS) {
    throw new Error(
      "CONFIGURED_CUSTOM_TOKENS is not defined in environment. Make sure to run the export step in the documentation.",
    );
  }

  const configFile = decodeBase64ToJson(
    process.env.CONFIGURED_CUSTOM_TOKENS || "",
  );
  const schema = await readSchema("configuredCustomTokens.schema.json");
  if (!validateJsonAgainstSchema(configFile, schema)) {
    throw new Error("encoded generateCustomTokenConfig.json is not valid.");
  }

  const tokens = configFile;
  const body =
    `import type { Token } from '$libs/token';\n` +
    `import { TokenType } from '$libs/token';\n\n` +
    `export const customToken: Token[] = ${_formatTokensToTsLiteral(tokens)};\n`;
  const out = await writeGenerated("customTokenConfig.ts", pluginName, body);
  logger.info(`Configured ${tokens.length} tokens.`);
  logger.info(`Formatted config file saved to ${out}`);
}

