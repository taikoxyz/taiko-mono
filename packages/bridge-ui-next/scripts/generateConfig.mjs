#!/usr/bin/env node

/**
 * bridge-ui-next config generator.
 *
 * Reproduces the five SvelteKit/Vite `generate*Config` plugins
 * (scripts/vite-plugins/* in the original bridge-ui) as a single standalone
 * prebuild step. For each config it performs the identical pipeline:
 *
 *   read process.env.<VAR> (base64) -> decodeBase64ToJson -> validateJsonAgainstSchema (ajv)
 *     -> build a TS source string (same export name + same enum-reference output)
 *     -> Prettier-format -> write to src/config/generated/<name>.ts
 *
 * Export names are kept EXACTLY as the original so the `$bridgeConfig`,
 * `$chainConfig`, `$relayerConfig`, `$customToken`, `$eventIndexerConfig`
 * aliases resolve unchanged:
 *   bridgeConfig.ts        -> export const routingContractsMap: RoutingMap
 *   chainConfig.ts         -> export const chainConfig: ChainConfigMap
 *   relayerConfig.ts       -> export const configuredRelayer: RelayerConfig[]
 *   customTokenConfig.ts   -> export const customToken: Token[]
 *   eventIndexerConfig.ts  -> export const configuredEventIndexer: EventIndexerConfig[]
 *
 * SKIP_ENV_VALIDATION=true emits the same empty fallbacks as the original
 * ({} for bridge/chain, [] for relayer/customToken/eventIndexer). The
 * customToken fallback keeps the original `Token[]` typing (via a `$libs/token`
 * type-only import) so consumers like `$libs/token/tokens.ts` type-check. The
 * non-skip path reproduces the original `$libs/*` type imports + enum
 * references verbatim.
 */
import { promises as fs } from "fs";
import path from "path";
import { fileURLToPath } from "url";

import { decodeBase64ToJson } from "./utils/decodeBase64ToJson.mjs";
import { formatSourceFile } from "./utils/formatSourceFile.mjs";
import { loadEnv } from "./utils/loadEnv.mjs";
import { PluginLogger } from "./utils/PluginLogger.js";
import { validateJsonAgainstSchema } from "./utils/validateJson.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(__dirname, "..");
const schemasDir = path.join(__dirname, "config", "schemas");
const generatedDir = path.join(packageRoot, "src", "config", "generated");

await loadEnv(packageRoot);

const skip = process.env.SKIP_ENV_VALIDATION === "true";

const readSchema = async (name) =>
  JSON.parse(await fs.readFile(path.join(schemasDir, name), "utf-8"));

const header = (pluginName) =>
  `// Generated by ${pluginName} on ${new Date().toLocaleString()}\n` +
  `// WARNING: Do not change this file manually as it will be overwritten\n`;

async function writeGenerated(fileName, pluginName, body) {
  const tsFilePath = path.join(generatedDir, fileName);
  await fs.mkdir(generatedDir, { recursive: true });
  await fs.writeFile(tsFilePath, `${header(pluginName)}${body}`);
  const formatted = await formatSourceFile(tsFilePath);
  await fs.writeFile(tsFilePath, formatted);
  return tsFilePath;
}

// ---------------------------------------------------------------------------
// bridgeConfig — generateBridgeConfig
// ---------------------------------------------------------------------------
const _formatBridgeObjectToTsLiteral = (obj) => {
  const formatValue = (value) => {
    if (typeof value === "string") return `"${value}"`;
    if (typeof value === "object") return JSON.stringify(value);
    return String(value);
  };

  const entries = Object.entries(obj);
  const formattedEntries = entries.map(([key, value]) => {
    const innerEntries = Object.entries(value);
    const innerFormattedEntries = innerEntries.map(([innerKey, innerValue]) => {
      const innerInnerEntries = Object.entries(innerValue);
      const innerInnerFormattedEntries = innerInnerEntries.map(
        ([innerInnerKey, innerInnerValue]) =>
          `${innerInnerKey}: ${formatValue(innerInnerValue)}`,
      );
      return `${innerKey}: {${innerInnerFormattedEntries.join(", ")}}`;
    });
    return `${key}: {${innerFormattedEntries.join(", ")}}`;
  });

  return `{${formattedEntries.join(", ")}}`;
};

async function generateBridgeConfig() {
  const pluginName = "generateBridgeConfig";
  const logger = new PluginLogger(pluginName);
  logger.info("Plugin initialized.");

  if (skip) {
    // Empty fallback, typed as RoutingMap to match the original Vite plugin
    // (generateBridgeConfig.ts) now that $libs/bridge is ported.
    const body =
      `import type { RoutingMap } from '$libs/bridge';\n\n` +
      `export const routingContractsMap: RoutingMap = {};\n`;
    const out = await writeGenerated("bridgeConfig.ts", pluginName, body);
    logger.info(`Skipped bridge.`);
    logger.info(`Formatted config file saved to ${out}`);
    return;
  }

  if (!process.env.CONFIGURED_BRIDGES) {
    throw new Error(
      "CONFIGURED_BRIDGES is not defined in environment. Make sure to run the export step in the documentation.",
    );
  }

  const configFile = decodeBase64ToJson(process.env.CONFIGURED_BRIDGES || "");
  const schema = await readSchema("configuredBridges.schema.json");
  if (!validateJsonAgainstSchema(configFile, schema)) {
    throw new Error("encoded configuredBridges.json is not valid.");
  }

  if (
    !configFile.configuredBridges ||
    !Array.isArray(configFile.configuredBridges)
  ) {
    logger.error(
      "configuredBridges is not an array. Please check the content of the configuredBridgesConfigFile.",
    );
    throw new Error();
  }

  const routingContractsMap = {};
  configFile.configuredBridges.forEach((item) => {
    if (!routingContractsMap[item.source]) {
      routingContractsMap[item.source] = {};
    }
    routingContractsMap[item.source][item.destination] = item.addresses;
  });

  const body =
    `import type { RoutingMap } from '$libs/bridge';\n\n` +
    `export const routingContractsMap: RoutingMap = ${_formatBridgeObjectToTsLiteral(routingContractsMap)};\n`;
  const out = await writeGenerated("bridgeConfig.ts", pluginName, body);
  logger.info(`Configured ${configFile.configuredBridges.length} bridges.`);
  logger.info(`Formatted config file saved to ${out}`);
}

// ---------------------------------------------------------------------------
// chainConfig — generateChainConfig (local LayerType enum, dedupe + validate)
// ---------------------------------------------------------------------------
const LayerType = { L1: "L1", L2: "L2", L3: "L3" };

const _formatChainObjectToTsLiteral = (obj) => {
  const formatValue = (value) => {
    if (typeof value === "string") {
      if (Object.values(LayerType).includes(value)) {
        return `LayerType.${value}`;
      }
      return `"${value}"`;
    }
    if (
      typeof value === "number" ||
      typeof value === "boolean" ||
      value === null
    ) {
      return String(value);
    }
    if (Array.isArray(value)) {
      return `[${value.map(formatValue).join(", ")}]`;
    }
    if (typeof value === "object") {
      return _formatChainObjectToTsLiteral(value);
    }
    return "undefined";
  };

  if (Array.isArray(obj)) {
    return `[${obj.map(formatValue).join(", ")}]`;
  }

  const entries = Object.entries(obj);
  const formattedEntries = entries.map(
    ([key, value]) => `${key}: ${formatValue(value)}`,
  );

  return `{${formattedEntries.join(", ")}}`;
};

async function generateChainConfig() {
  const pluginName = "generateChainConfig";
  const logger = new PluginLogger(pluginName);
  logger.info("Plugin initialized.");

  const enumDecl = `enum LayerType {\n  L1 = 'L1',\n  L2 = 'L2',\n  L3 = 'L3',\n}\n`;

  if (skip) {
    const body = `export const chainConfig: Record<string, unknown> = {};\n`;
    const out = await writeGenerated("chainConfig.ts", pluginName, body);
    logger.info(`Skipped chains.`);
    logger.info(`Formatted config file saved to ${out}`);
    return;
  }

  if (!process.env.CONFIGURED_CHAINS) {
    throw new Error(
      "CONFIGURED_CHAINS is not defined in environment. Make sure to run the export step in the documentation.",
    );
  }

  const configFile = decodeBase64ToJson(process.env.CONFIGURED_CHAINS || "");
  const schema = await readSchema("configuredChains.schema.json");
  if (!validateJsonAgainstSchema(configFile, schema)) {
    throw new Error("encoded configuredChains.json is not valid.");
  }

  if (
    !configFile.configuredChains ||
    !Array.isArray(configFile.configuredChains)
  ) {
    console.error(
      "configuredChains is not an array. Please check the content of the configuredChainsConfigFile.",
    );
    throw new Error();
  }

  const chainConfig = {};
  configFile.configuredChains.forEach((item) => {
    for (const [chainIdStr, config] of Object.entries(item)) {
      const chainId = Number(chainIdStr);
      const type = config.type;

      if (Object.prototype.hasOwnProperty.call(chainConfig, chainId)) {
        logger.error(
          `Duplicate chainId ${chainId} found in configuredChains.json`,
        );
        throw new Error();
      }

      if (!Object.values(LayerType).includes(config.type)) {
        logger.error(
          `Invalid LayerType ${config.type} found for chainId ${chainId}`,
        );
        throw new Error();
      }

      chainConfig[chainId] = { ...config, type };
    }
  });

  const body =
    `import type { ChainConfigMap } from '$libs/chain';\n\n` +
    `${enumDecl}\n` +
    `export const chainConfig: ChainConfigMap = ${_formatChainObjectToTsLiteral(chainConfig)};\n`;
  const out = await writeGenerated("chainConfig.ts", pluginName, body);
  logger.info(`Configured ${Object.keys(chainConfig).length} chains.`);
  logger.info(`Formatted config file saved to ${out}`);
}

